/******************************************************************************/
/*!
  \author
    KiKyeong Lim

  \brief 
    Take angle as degree and return sin value
  
  \param degree
    angle as degree

  \parma
    float - sin value
    
*/
/******************************************************************************/
float Sin(float degree)
{
  int angle,accuracy,flip;

  if(degree < -EPSILON)
  {
    flip = -1;
    degree *= flip;
  }
  else
  {
    flip = 1;
  }
  angle = static_cast<int>(degree*ACCURACY_ADJUSTOR);
  accuracy = angle%ACCURACY_ADJUSTOR;
  accuracy /= ADJUSTOR_OVER_ACCURACY;
  angle /= 100;
  angle %= ONE_PERIOD;

  return flip*SIN_TABLE[ACCURACY*angle+accuracy];
}
/******************************************************************************/
/*!
  \author
    KiKyeong Lim

  \brief 
    Take angle as degree and return cos value
  
  \param degree
    angle as degree

  \parma
    float - cos value
    
*/
/******************************************************************************/
float Cos(float degree)
{
  int angle,accuracy;

  if(degree < EPSILON)
  {
    degree *= -1.0f;
  }

  angle = static_cast<int>(degree*ACCURACY_ADJUSTOR);
  accuracy = angle%ACCURACY_ADJUSTOR;
  accuracy /= ADJUSTOR_OVER_ACCURACY;
  angle /= 100;
  angle %= ONE_PERIOD;

  return COS_TABLE[ACCURACY*angle+accuracy];
}